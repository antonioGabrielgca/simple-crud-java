    public void popTable() throws SQLException {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);

        Controler controler = new Controler();

        for (User u : controler.readTable()) {

            model.addRow(new Object[]{
                u.getId(),
                u.getName(),
                u.getAge(),
                u.getOccupation(),
                u.getSalary()
            });

        }

    }

    public void popTableOfName(String name) {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);

        Controler controler = new Controler();

        controler.readTableOfVariable(name).forEach((u) -> {

            model.addRow(new Object[]{
                u.getId(),
                u.getName(),
                u.getAge(),
                u.getOccupation(),
                u.getSalary()
            });

        });

    }

