    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        Controler controler = new Controler();
        User user = new User();

        if (!jTextField5.getText().isEmpty()) {

            user = controler.getInfo(jTextField5.getText());

            if (user.getAge() != -1) {
                jLableName.setText(user.getName());
                jLabelAge.setText(String.valueOf(user.getAge()));
                jLabelOccupation.setText(user.getOccupation());
                jLabelSalary.setText(String.valueOf(user.getSalary()));
                jLabelID.setText(String.valueOf(user.getId()));
            } else {
                cleanUpdateFrame();
            }

        }

    }//GEN-LAST:event_jButton5ActionPerformed

